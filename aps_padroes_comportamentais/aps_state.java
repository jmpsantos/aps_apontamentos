/* State:  Padrão que permite um objeto alterar o seu comportamento dinamicamente quando o seu estado muda
 *      
 *         Utilizar quando: - o objeto comportasse de forma diferente tendo em conta o seu estado, existe um grande número de estados e o código muda frequentemente com base em cada estado
 * 
 *                          - a classe está sobrecarregada com código de condicionais
 * 
 *          Vantagens: - permite adicionar novos estados sem mudar o contexto das classes
 *                     - elimina repetição de código nas condicionais
 *                     - organiza o funcionamento do código á volta dos estados
 * 
 *          Desvantagens: -é desnecessariamente trabalhoso se existirem poucos estados
 * 
 */


 
/**
 * Common interface for all states.
 */
public abstract class State {
    Player player;

    /**
     * Context passes itself through the state constructor. This may help a
     * state to fetch some useful context data if needed.
     */
    State(Player player) {
        this.player = player;
    }

    public abstract String onLock();
    public abstract String onPlay();
    public abstract String onNext();
    public abstract String onPrevious();
}

/**
 * Concrete states provide the special implementation for all interface methods.
 */
public class LockedState extends State {

    LockedState(Player player) {
        super(player);
        player.setPlaying(false);
    }

    @Override
    public String onLock() {
        if (player.isPlaying()) {
            player.changeState(new ReadyState(player));
            return "Stop playing";
        } else {
            return "Locked...";
        }
    }

    @Override
    public String onPlay() {
        player.changeState(new ReadyState(player));
        return "Ready";
    }

    @Override
    public String onNext() {
        return "Locked...";
    }

    @Override
    public String onPrevious() {
        return "Locked...";
    }
}


